#Este algoritmo é uma calculadora simples
v1 = float(input ("Introduza o valor 1: "))
v2 = float(input ("Introduza o valor 2: "))

Soma = v1 + v2
Subtração = v1-v2
Multiplicação = v1*v2
Divisão = v1/v2

print (f"A soma de {v1} com  {v2} é {(v1 + v2):.3f}") 
print (f"A subtração de {v1} com  {v2} é {(v1 - v2):.3f}")
print (f"A multiplicação de {v1} com  {v2} é {(v1 * v2):.3f}")
print (f"A divisão de {v1} com  {v2} é {(v1 / v2):.3f}")